package wtn;

public class eight {
	public static void main(String args[]){
        int i=1;
        int n =Integer.parseInt(args[0]);
        while(i<=n)
        {
            int j= 1;
            while(j<=i){
                System.out.println(" * ");
                j++;
        }
        System.out.println();
        i++;
   }           
   }

}
